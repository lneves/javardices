package trade.ws;

public class Login
{
	private String userID;
	private String password;

	public String getUserId()
	{
		return userID;
	}

	public void setUserId(String userID)
	{
		this.userID = userID;
	}

	public String getPassword()
	{
		return password;
	}

	public void setPassword(String password)
	{
		this.password = password;
	}
}
